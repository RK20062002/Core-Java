package SamplePrograms.src.org.Exception;

/*
 * Exception is an unexpected event which occurs the execution of a program
 * 
 * Types :-
 * 		Unchecked Exception:
 * 			run time occurs
 * 
 * 		Checked Exception :
 * 			Compile time occurs
 * */

public class Arithmetic {
	
	public static void main(String[] args) {
		
		// 1. Arithmetic Exception
		
		System.out.println(1);
		System.out.println(2/0); // --------> '/' by 0 so exception
		System.out.println(3);
		
	}

}
